#include<iostream>
using namespace std;
struct ListNode {
    int val;
    ListNode *next;
    ListNode() : val(0), next(nullptr) {}
    ListNode(int x) : val(x), next(nullptr) {}
    ListNode(int x, ListNode *next) : val(x), next(next) {}
};
// @lc code=start
/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeElements(ListNode* head, int val) {
        if(head==nullptr){
            return nullptr;
        }
        ListNode* dum=new ListNode(0);
        dum->next=head;
        ListNode* p1=head;
        ListNode* p2=dum;
        while(p1){
            if(p1->next==nullptr&&p1->val==val){
                p2->next=nullptr;
            }
            if(p1->val==val&&p1->next!=nullptr){
                ListNode* temp=p1->next;
                p2->next=p1->next;
                p1->next=nullptr;
                p1=temp;
            }
            else{
                p1=p1->next;
                p2=p2->next;
            }
        }
        return dum->next;
    }
};
